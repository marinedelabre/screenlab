            <h1>Addictions</h1>
    </div>

    <div class="question-reponse">

        <div class="question">
            <p>La cyber-addiction existe-t-elle vraiment ?</p>
        </div>

        <div class="reponse">
            <p>On définit l’utilisation problématique d’Internet et des nouvelles technologies comme étant «une utilisation des technologies ou des moyens de communication offerts par Internet qui engendre des difficultés chez l’individu» (Sergerie, 2005). «Cette surutilisation amènerait un sentiment de détresse et des difficultés au niveau psychologique, social ou professionnel» (Caplan, 2002; Young, 1998, 2004). Selon Griffith (1998), certains critères doivent être considérés dans l’examen de l’interaction entre l’humain et la technologie.</p>
        </div>

        <div class="question">
            <p>Et concernant le prédominance des écrans ?</p>
        </div>

        <div class="reponse">
            <p>Le comportement ou l’activité occupe une place prédominante dans la vie de l’individu, souvent au détriment d’activités ou de relations réelles. Ex.: pensées récurrentes ou envahissantes concernant la dernière séance d’Internet ou anticipation des séances à venir.</p>
        </div>

        <div class="question">
            <p>Y-a-t-il des modifications de l’humeur ?</p>
        </div>

        <div class="reponse">
            <p>Conséquence du comportement ou de l’activité. Par exemple, impression d’apaisement ou d’engourdissement euphorique.</p>
        </div>

        <div class="question">
            <p>Et au niveau de la tolérence ?</p>
        </div>

        <div class="reponse">
            <p>Besoin d’augmenter la quantité de temps consacré à l’activité pour obtenir les mêmes effets. Par exemple, passer plus de temps en ligne, investir des montants d’argent plus élevés pour les gageures, etc.</p>
        </div>

        <div class="question">
            <p>Quels sont les symptômes de manque ?</p>
        </div>

        <div class="reponse">
            <p>Sensations désagréables ressenties lorsqu’il y a cessation ou réduction dans la fréquence ou la durée d’utilisation. Par exemple, éprouver un sentiment de vide intérieur ou être irritable.</p>
        </div>

        <div class="question">
            <p>Cela peut-il créer des conflits ?</p>
        </div>

        <div class="reponse">
            <p>Entre le milieu de travail, le réseau social, la vie familiale et l’individu. Par exemple, mentir à ses proches au sujet de son utilisation d’Internet.</p>
        </div>

        <div class="question">
            <p>Y-a-t-il des cas de rechute ?</p>
        </div>

        <div class="reponse">
            <p>Efforts répétés, mais infructueux pour contrôler ou réduire le temps passé en ligne.</p>
        </div>

    </div>


